void my_flush_cb(lv_display_t * display, const lv_area_t * area, uint8_t * px_map)
{
    /* The most simple case (also the slowest) to send all rendered pixels to the
     * screen one-by-one.  `put_px` is just an example.  It needs to be implemented by you. */
    uint16_t * buf16 = (uint16_t *)px_map; /* Let's say it's a 16 bit (RGB565) display */
    int32_t x, y;
    for(y = area->y1; y <= area->y2; y++) {
        for(x = area->x1; x <= area->x2; x++) {
            ili9341_drawPixel(&displayLcd,(uint16_t) x, (uint16_t) y, *buf16);
            buf16++;
        }
    }

    /* IMPORTANT!!!
     * Inform LVGL that flushing is complete so buffer can be modified again. */
    lv_display_flush_ready(display);
}

void delay_ms(uint16_t time) {
	__HAL_TIM_SET_COUNTER(&htim3, 0);
	while(__HAL_TIM_GET_COUNTER(&htim3) <= time);
}

void button_clicked() {
	switch (idx) {
		case 0:
			lv_obj_set_style_bg_color(lv_screen_active(), lv_color_hex(COLOR_GREEN), LV_PART_MAIN);
			break;
		case 1:
			lv_obj_set_style_bg_color(lv_screen_active(), lv_color_hex(COLOR_RED), LV_PART_MAIN);
			break;
		case 2:
			lv_obj_set_style_bg_color(lv_screen_active(), lv_color_hex(COLOR_WHITE), LV_PART_MAIN);
			break;
		case 3:
			lv_obj_set_style_bg_color(lv_screen_active(), lv_color_hex(COLOR_MAGENTA), LV_PART_MAIN);
			break;
		default:
			break;
	}
	idx++;
}
